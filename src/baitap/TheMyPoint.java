package baitap;

public class TheMyPoint {
    private int x=0,y=0;
    public TheMyPoint(){};
    public TheMyPoint(int x,int y){
        this.x=x;
        this.y=y;
    }
    int getX(){
        return x;
    }
    void setX(int x){
        this.x=x;
    }
    int getY(){
        return y;
    }
    void setY(int y){
        this.y=y;
    }

}
